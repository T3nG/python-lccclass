'''
變數命名規則
題外話，八歲前改名字才有用！
變數的命名很重要，能一目瞭然最好
1. 使用匈牙利命名法(Java慣用法): 首字單字小寫，第二單字開始首字大寫
    price
    applePrice
    thisIsABook
    請不要稱駝峰法，沒水準
2. 中間不可以有空格， +-*/@$%^&
    this is a book <===錯
3. 首字不可以為數字
    58Score <===錯
4. 中間可以為 "_"
    this_is_a_book : c的慣用法
5. 首字可以為底線，但禁止
    _score : 系統級變數
6. 類別名稱，首字大寫
    class Pokemon
'''