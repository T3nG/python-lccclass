# 函數參數有 =10, 稱為預設值
# 調用端若有設定y, 則以調用端的值為準
# 若調用端沒有傳入y值, 則以預設值為準
def add(x,y=10):    # 被調用端
    return  x+y

# 調用函數時, 傳入參數個數需與被調用端相同
# 調用 : 大陸用語
a=add(10)           # 調用端
b=add(10,500)
print(a,b)

# 錯誤的種類
# 在編寫程式時, 有紅色底線, 稱為編譯時期錯誤(compile error)
# 執行時, 才發生錯誤, 稱為執行時期錯誤(run time error), 程式會閃退
# run time error時常要查找 log 才能 debug